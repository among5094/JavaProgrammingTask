
public class CircleArea2261013 {
	
	public static void main(String[] agar) { //main method 시작
		
		final double PI = 3.14; //상수
		double r=10.2; //r -> radius
		double circleArea=r*r*PI; //원 면적 계산
		
		//원 넓이 프린트
		System.out.println("radius: "+ r+",");
		System.out.println("circle area = "+ circleArea);
	
		
	}//end main method

}//class닫기
